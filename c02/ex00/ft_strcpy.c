#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int main()
{
	char a[] = "kdsjask";
	char b[] = "dskfd";
	printf("%s", ft_strcpy(a,b));

	return 0;
}

